char	*ft_strrchr(const char *s, int c)
{
	int		i_len;

	i_len = 0;
	while (s[i_len] != '\0')
		i_len++;
	while (i_len >= 0)
	{
		if (s[i_len] == (char)c)
		{
			return ((char *)&s[i_len]);
		}
		i_len--;
	}
	return (0);
}
